#!/usr/bin/env python3
"""
Module contains the function `concat`"""


def concat(str1: str, str2: str) -> str:
    """
    function: concat(str1: str, str2: str) -> str

    Concatenates two strings

    Args
    ----
    str1: string
    str2: string

    Returns
    -------
    A concatenation of both strings
    """
    return str1 + str2
